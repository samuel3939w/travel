      showGallary: false,
    };
  },
  components: {
    CommonGallary,
    FadeAnimation,
  },
  methods: {
    handleBannerClick() {
      this.showGallary = true;
    },
    handleGallaryClose() {
      this.showGallary = false;
    },
  },
};
</script>

